#include "libft.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*element;

	if (!lst)
		return ;
	while (*lst != 0)
	{
		element = *lst;
		*lst = element->next;
		free(element);
	}
	*lst = NULL;
}
